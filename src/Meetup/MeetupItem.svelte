<script>
    import meetups from "./meetup-store";
    import { createEventDispatcher } from "svelte";
    import Button from "../UI/Button.svelte";
    import Badge from "../UI/Badge.svelte";
    import Loader from "../UI/Loader.svelte";
    const dispatch = createEventDispatcher();

    export let id;
    export let title;
    export let subtitle;
    export let description;
    export let imageUrl;
    export let address;
    export let contactEmail;
    export let isFavorite;

    let isLoading = false;
    function toogleFavorite() {
        isLoading = true;
        fetch(
            `https://svelte-mishaszu-course-default-rtdb.europe-west1.firebasedatabase.app/meetup/${id}.json`,
            {
                method: "PATCH",
                body: JSON.stringify({ isFavorite: !isFavorite }),
            }
        )
            .then((res) => {
                if (!res.ok) {
                    throw new Error(
                        "An error occurred while trying to get the meetup data."
                    );
                }
                setTimeout(() => {
                    isLoading = false;
                    meetups.toogleFavorite(id);
                }, 500);
            })
            .catch((err) => {
                isLoading = false;
                console.error(err);
            });
    }
</script>

<article>
    <header>
        <h1>
            {title}
            {#if isFavorite}
                <Badge>FAVORITE</Badge>
            {/if}
        </h1>
        <h2>{subtitle}</h2>
        <p>{address}</p>
    </header>
    <div class="image">
        <img src={imageUrl} alt={title} />
    </div>
    <div class="content">
        <p>{description}</p>
    </div>
    <footer>
        <!-- <Button href="mailto:{contactEmail}">Contact</Button> -->
        <Button
            type="button"
            mode="outline"
            on:click={() => dispatch("edit", id)}>Edit</Button
        >
        {#if isLoading}
            <span>Loading...</span>
        {:else}
            <Button
                mode="outline"
                color={isFavorite ? null : "success"}
                type="button"
                on:click={toogleFavorite}
            >
                {isFavorite ? "Unfavorite" : "Favorite"}
            </Button>
        {/if}
        <Button on:click={() => dispatch("showdetail", id)} type="button"
            >Show Details</Button
        >
    </footer>
</article>

